// === IS EQUAL TO
// !== NOT EQUAL 2
// > GREATER THAN
// < SMALLER THAN
// <= SMALLER THAN OR EQUAL TO
// >= SMALLER THAN OR EQUAL TO

var a = 1 //number
var b = "1" //string


// 1-Checks if the data types are matching: **
if (a === b) {
    console.log("yes")
} else {
    console.log("NOT")
} //prints NOT


// 2-Checks only if the data values are matching: **
if (a == b) {
    console.log("yes")
} else {
    console.log("no")
} //prints yes